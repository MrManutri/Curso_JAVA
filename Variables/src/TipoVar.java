public class TipoVar {
    public static void main(String[] args) {
        System.out.println("******** Uso de var en Java **********");
        //Sin uso
        String nombre1 = "Juan";
        System.out.println("nombre1 = " + nombre1);
        
        //Uso de var
        var nombre2 = "Carlos";
        System.out.println("nombre2 = " + nombre2);
    }
}
